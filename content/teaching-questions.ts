for (const content of Object.values(teachingQuestionContent)) {
  teachingQuestionContentSchema.parse(content);
  content.answers = [content.answer];
}

export const easyQuestionIds = Object.values(teachingQuestionContent)
  .filter((question) => question.difficulty === "easy")
  .map((question) => question.findingId);
export const mediumQuestionIds = Object.values(teachingQuestionContent)
  .filter((question) => question.difficulty === "medium")
  .map((question) => question.findingId);
export const hardQuestionIds = Object.values(teachingQuestionContent)
  .filter((question) => question.difficulty === "hard")
  .map((question) => question.findingId);

export function activeQuestionIdsForDifficulty(
  difficulty: "easy" | "medium" | "hard",
  random = Math.random,
) {
  const pool =
    difficulty === "easy"
      ? easyQuestionIds
      : difficulty === "medium"
        ? mediumQuestionIds
        : hardQuestionIds;
  if (difficulty !== "easy") return [...pool];
  // The ten Easy questions are paired by garden target. Pick one from each
  // pair so the random session still opens exactly five visible targets.
  return Array.from({ length: 5 }, (_, index) =>
    pool[index + (random() < 0.5 ? 0 : 5)],
  );
}
