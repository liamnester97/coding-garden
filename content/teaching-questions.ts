import { z } from "zod";

export const teachingQuestionContentSchema = z.object({
  findingId: z.string().min(1),
  difficulty: z.enum(["easy", "medium", "hard"]),
  gradeBand: z.enum(["grades-1-5", "grades-6-8", "grades-9-12"]),
  questionType: z.enum(["find-it", "plan-it", "execute-it"]),
  codeExcerpt: z.string().min(1).max(1200),
  language: z.literal("python"),
  prompt: z.string().min(1),
  choices: z.array(z.string().min(1).max(180)).length(4),
  answer: z.string().min(1),
  hint: z.string().min(1),
  example: z.string().min(1),
  explanation: z.string().min(1),
  recap: z.string().min(1),
  actionLabel: z.string().min(1),
  beforeCode: z.string().min(1),
  afterCode: z.string().min(1),
  proposedFix: z.string().min(1),
  reanalysisEvidence: z.string().min(1),
});

